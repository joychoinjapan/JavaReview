package review.java;

public class FloatDemo {
	public static void main(String[] args) {
		//定义一个单精度浮点型变量
		float f = 1234.3f;
		System.out.println("f="+f);
		
		//float 表示范围小，赋值给double没有问题
		double d = 3444.34f;
		System.out.println("d="+d);
		
		//将整型赋值给浮点型
		double d1 = 123;
		System.out.println("d1="+d1);

		
	}
}
